# Το παραπάνω κομμάτι κώδικα δημιουργεί ένα νέο αντικείμενο "Person" από την κλάση "Person" και καλεί τη μέθοδο
# "greet". Έτσι, εκτυπώνεται το μήνυμα χαιρετισμού με το όνομα που έχει δωθεί.
