# Ας δημιουργήσουμε μια κλάση με το όνομα 'Car' με χαρακτηριστικά όπως
# 'make', 'model', 'year'.
# Αρχικοποιήστε ένα αντικείμενο της κλάσης με τις λεπτομέρειες του αγα-
# πημένου μας αυτοκινήτου.
# Στη συνέχεια, προσθέστε μια μέθοδο με το όνομα 'start_engine' που ε-
# κτυπώνει ένα μήνυμα όπως
#           Το [year] [make] [model] ξεκινάει τη μηχανή

class Car:
    def __init__(self, make, model, year):
        self.make = make
        self.model = model
        self.year = year
    
    def start_engine(self):
        print(f'Το {self.year} {self.make} {self.model} ξεκινάει τη μηχανή')


# Αρχικοποίηση ενός αντικειμένου και κλήση της μεθόδου start_engine
my_car = Car('Toyota', 'Corolla', 2022)
my_car.start_engine()

