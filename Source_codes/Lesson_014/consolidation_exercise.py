# 'model' και 'year' στον κατασκευαστή '__init__'. Ορίζεται επίσης η μέθοδος
# 'start_engine', η οποία απλώς να εκτυπώνει ένα μύνημα χρησιμοποιώντας τις
# τιμές των χαρακτηριστικών.
#
# Στη συνέχεια, δημιουργούμε ένα αντικείμενο 'my_car' με τη μάρκα 'Toyota'
# το μοντέλο 'Corolla' και το έτος 2022. Τέλος, καλούμε τη μέθοδο 'start_engine'
# στο 'my_car', η οποία εκτυπώνει το επιθυμητό μήνυμα.


