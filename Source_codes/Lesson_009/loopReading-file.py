# είναι αναμενόμενο να μην καλούμε την readline() για να διαβάζει
# λενα κείμενο γραμμή-γραμμή.
#
# αυτό μπορεί να επιτευχθεί με την χρήση μίας επαναληπτικής δια-
# δικασίας for loop


f = open('myfile.txt', 'r')

# for loop --> read line-to-line the opened file
for line in f:
    print(line, end = '')
    # για κάθε γραμμή του αρχείου:
    #   διαβάζει την γραμμή line και την τυπώνει με την χρήση της
    #   συνάρτησης print()

f.close()